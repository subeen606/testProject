DROP TABLE SCHEDULE
CASCADE CONSTRAINTS;

DROP SEQUENCE SCHEDULE_SEQ;

CREATE TABLE SCHEDULE(
	SCHEDULE_SEQ NUMBER(5) PRIMARY KEY,	
	SCHEDULE_DATE VARCHAR2(50) NOT NULL,	/* 년,월,일 까지만 */
	SCHEDULE_TIME VARCHAR2(50) NOT NULL,	/* 시,분까지 */
	SCHEDULE_PLACE VARCHAR2(200) NOT NULL,
	SCHEDULE_TITLE VARCHAR2(200) NOT NULL,
	SCHEDULE_CONTENT VARCHAR2(200) NOT NULL,
	TICKETLINK VARCHAR2(1000) NOT NULL,
	SCHEDULE_POSTER VARCHAR2(100) NOT NULL
);

CREATE SEQUENCE SCHEDULE_SEQ
START WITH 1
INCREMENT BY 1;

SELECT * FROM SCHEDULE;

SELECT SCHEDULE_SEQ, SCHEDULE_DATE, SCHEDULE_TIME, 
SCHEDULE_PLACE, SCHEDULE_TITLE, SCHEDULE_CONTENT, TICKETLINK, SCHEDULE_POSTER 
FROM ( SELECT ROW_NUMBER()OVER(ORDER BY SCHEDULE_SEQ DESC) AS RNUM, 
SCHEDULE_SEQ, SCHEDULE_DATE, SCHEDULE_TIME, 
SCHEDULE_PLACE, SCHEDULE_TITLE, SCHEDULE_CONTENT, TICKETLINK, SCHEDULE_POSTER 
FROM SCHEDULE
)