DROP TABLE NEWS
CASCADE CONSTRAINTS;

DROP SEQUENCE NEWS_SEQ;


CREATE TABLE NEWS(		-- 타미노(가수)의 새로운 소식 테이블
	MENU_NUM NUMBER(5) NOT NULL, /* photo:0, video:1  */
	TITLE VARCHAR2(50) NOT NULL,
	CONTENT VARCHAR2(4000) NOT NULL,
	NEWS_SEQ NUMBER(5) PRIMARY KEY,	
	NEWS_DATE DATE NOT NULL,
	NEWS_READCOUNT NUMBER(5) NOT NULL,
	DEL NUMBER(1) NOT NULL,		/* 평상시:0 , 삭제시:1 */
	FILENAME VARCHAR2(4000) NOT NULL
);


CREATE SEQUENCE NEWS_SEQ
START WITH 1
INCREMENT BY 1;

SELECT * FROM NEWS;


















